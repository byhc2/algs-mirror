#include <cmath>
#include "algsmath.h"

namespace algs
{

Double AlgsMath::log(Double i)
{
    return ::log(i);
}

Double AlgsMath::sin(Double a)
{
    return ::sin(a);
}

Double AlgsMath::cos(Double a)
{
    return ::cos(a);
}

Double AlgsMath::pow(Double x, Double y)
{
    return ::pow(x, y);
}

}
