#include <iostream>

using namespace std;

int main(int argc, char *argv[])
{
    cout << " 3  %  2 = " << 3 % 2 << endl;
    cout << "-3  %  2 = " << -3 % 2 << endl;
    cout << " 3  % -2 = " << 3 % -2 << endl;
    cout << "-3  % 10 = " << -3 % 10 << endl;
    cout << "-3  / 10 = " << -3 / 10 << endl;
    cout << "-10 / 3  = " << -10 / 3 << endl;
    int a = -576;
    cout << "-(-576) / 512 + 1  = " << -a / 512 + 1 << endl;

    return 0;
}
